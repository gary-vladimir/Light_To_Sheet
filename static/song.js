window.midiToName = function (m) {
  const names = ["C", "C#", "D", "D#", "E", "F", "F#", "G", "G#", "A", "A#", "B"];
  const oct = Math.floor(m / 12) - 1;
  return names[m % 12] + oct;
};
window.midiIsBlack = function (m) {
  return [1, 3, 6, 8, 10].includes(m % 12);
};
